"""
================================================================================
 main.py — 应用主入口（总装车间）
================================================================================

 这是整个应用的"总装车间"，只做三件事：
   1. 创建 FastAPI 应用实例
   2. 组装各组件（异常处理器、中间件、路由）
   3. 启动服务

 这里不写任何业务逻辑。业务逻辑都在 routers/ 和 schemas/ 里。
 这个文件只负责"装配"——就像搭积木一样把各个模块拼在一起。

 ────────────────────────────────────────────────────────────────────────────
 注册顺序（重要！）

 组件的注册顺序是有讲究的，就像穿衣服——先穿内衣再穿外套：

   1. 异常处理器（最先注册）
      → 确保在请求的任何阶段，异常都能被正确捕获

   2. 中间件（其次注册）
      → 每个请求都会经过中间件，洋葱模型的"外皮"

   3. 路由（最后注册）
      → 实际处理业务逻辑的地方
      → 路由中的异常会被前面的异常处理器兜底

 ────────────────────────────────────────────────────────────────────────────
 启动方式

 在命令行中运行：
   uvicorn app.main:app --reload

 或者直接运行本文件：
   python app/main.py
================================================================================
"""

# ============================================================================
# 导入所需模块
# ============================================================================

# 系统库
import time

# FastAPI 核心
from fastapi import FastAPI, Request

# 路由层 —— 从 routers/task.py 中导入 router
from app.routers.task import router as task_router

# 工具层 —— 异常处理器和日志
from app.utils.exceptions import register_exception_handlers
from app.utils.logger import logger


# ============================================================================
# 第一步：创建 FastAPI 应用实例
# ============================================================================
# 这个 app 就是整个应用的"心脏"。
# uvicorn 启动时会加载这个 app 变量。
#
# title 和 description 会显示在 /docs 交互式文档的页面上。
# ============================================================================
app = FastAPI(
    title="任务管理系统",
    description="第2周工程化分层架构实战 — APIRouter + 全局异常 + 中间件 + 结构化日志",
    version="2.0.0",
)


# ============================================================================
# 第二步：注册异常处理器
# ============================================================================
# 必须在路由和中间件之前注册，这样异常处理器才能覆盖整个请求生命周期。
# 这里调用的是 utils/exceptions.py 中定义的注册函数。
# ============================================================================
register_exception_handlers(app)
logger.info("✅ 异常处理器注册完成")


# ============================================================================
# 第三步：注册中间件
# ============================================================================

# ──────────────────────────────────────────────────────────────────────────
# 中间件 1：请求耗时统计中间件
# ──────────────────────────────────────────────────────────────────────────
# 这个中间件做的事：
#   1. 请求进来时，记下当前时间
#   2. 放行请求（让请求继续走向路由）
#   3. 请求处理完后，再次获取时间，算出耗时
#   4. 把耗时写入响应头 X-Process-Time
#   5. 打印一条结构化访问日志
#
# 洋葱模型原理：
#   请求从外面进来，经过中间件 → 到路由处理 → 再原路返回。
#   中间件中写在 await call_next 之前的代码在"进去的路上"执行，
#   写在 await call_next 之后的代码在"回来的路上"执行。
#
#   call_next 就是"下一个环节"的引用。
#   await 是 Python 异步语法，表示"等 call_next 执行完再继续"。
# ──────────────────────────────────────────────────────────────────────────

@app.middleware("http")
async def process_time_middleware(request: Request, call_next):
    """
    请求耗时统计中间件。

    这个函数对每个 HTTP 请求都会执行一次。
    request:  当前请求对象，包含 URL、方法、请求头等信息
    call_next: 指向"下一个环节"的函数（下一个中间件或路由处理函数）
    """

    # ---- 请求前（进去的路上）----
    # time.perf_counter() 返回一个高精度的时间值（单位：秒）
    # 用它来记下请求开始的时间点
    start_time = time.perf_counter()

    # 打印请求进来的日志
    # 这里记 INFO 级别，因为这是正常的访问记录
    logger.info(f"→ {request.method} {request.url.path}")

    # ---- 放行（让请求继续）----
    # await call_next(request) 会：
    #   1. 如果有下一个中间件，调用它
    #   2. 如果没有下一个中间件，调用路由处理函数
    # 然后返回一个 Response 对象（响应）
    response = await call_next(request)

    # ---- 响应后（回来的路上）----
    # 计算耗时：现在的时间 - 之前记下的时间
    elapsed = time.perf_counter() - start_time

    # 在响应头中添加 X-Process-Time 字段
    # 这样前端可以通过 response.headers["X-Process-Time"] 看到处理耗时
    # round(elapsed, 4) 保留 4 位小数，比如 0.0356
    response.headers["X-Process-Time"] = f"{elapsed:.4f}"

    # 打印结构化访问日志
    # 格式：← GET /tasks [200] 0.0356s
    logger.info(
        f"← {request.method} {request.url.path} "
        f"[{response.status_code}] {elapsed:.4f}s"
    )

    return response


# ============================================================================
# 第四步：注册路由
# ============================================================================
# 把 routers/task.py 中定义的所有接口挂载到 app 上。
# 注意这里 include_router 时没有 prefix，
# 因为 prefix 已经在 APIRouter(prefix="/tasks") 定义好了。
# ============================================================================
app.include_router(task_router)
logger.info("✅ 路由注册完成")


# ============================================================================
# 根路径欢迎接口
# ============================================================================
# 这个接口直接挂在 app 上，没有放在路由文件里。
# 因为它是"根路径"，不属于任何业务模块。
# 这种情况也符合分层原则——特殊的归特殊的处理。
# ============================================================================

@app.get("/", summary="根路径欢迎")
def root():
    """
    根路径入口，返回欢迎信息。
    访问 http://127.0.0.1:8000/ 可以看到。
    """
    logger.info("访问根路径")
    return {"message": "欢迎使用任务管理系统"}


# ============================================================================
# 直接运行本文件时的启动入口
# ============================================================================
# 如果用户直接执行 python app/main.py，
# 下面的代码会启动 uvicorn 服务器。
#
# 但更推荐用命令行启动：
#   uvicorn app.main:app --reload
# 这样 --reload 热重载功能更好用。
# ============================================================================

if __name__ == "__main__":
    import uvicorn

    logger.info("🚀 启动服务: http://127.0.0.1:8000")
    logger.info("📖 接口文档: http://127.0.0.1:8000/docs")
    uvicorn.run("app.main:app", reload=True)
