"""
================================================================================
 schemas/task.py — Pydantic 数据模型
================================================================================

 这个文件定义了"任务"的数据结构，也就是 API 接口收到的数据和返回的数据长什么样。
 你可以把它理解成"快递单模板"：
   - TaskCreate：用户下单时要填的表（不需要填快递单号）
   - Task      ：快递公司返回给用户的回执（包含快递单号）

 Pydantic 是 FastAPI 用的数据校验库，它保证：
   1. 数据类型正确（比如 title 必须是字符串）
   2. 数据范围合法（比如 priority 必须在 1~5 之间）
   3. 必填字段都有值（比如 title 不能为空）
     ── 以上任一条不满足，FastAPI 自动返回 422 状态码 + 错误信息
================================================================================
"""

from pydantic import BaseModel, Field


# ============================================================================
# TaskCreate — 创建任务时的"请求体"模型
# ============================================================================
# 继承 BaseModel：表示这是一个 Pydantic 数据模型
# 这个模型是"请求体"，用户在 POST /tasks 时发送的就是这个格式的 JSON
# 它不包含 task_id，因为 task_id 是服务端自动生成的，用户不用也管不到
# ============================================================================

class TaskCreate(BaseModel):
    """
    创建任务时，客户端（前端）需要发送的数据格式。

    通俗理解：用户在前端表单里填写的内容就对应到这里。
    """

    # ========================================================================
    # title: 任务标题（必填）
    # ========================================================================
    # Field(...) 表示这是必填字段（三个点是 Python 的 Ellipsis，意思是"没有默认值"）
    # min_length=1    — 标题至少 1 个字符（不能是空字符串）
    # max_length=50   — 标题最多 50 个字符
    # description     — 这个描述会显示在 /docs 交互式文档里，帮助前端开发者理解
    # ========================================================================
    title: str = Field(
        ...,
        min_length=1,
        max_length=50,
        description="任务标题，长度1-50个字符",
    )

    # ========================================================================
    # description: 任务描述（可选）
    # ========================================================================
    # str | None 表示这个字段可以是字符串，也可以是 None（不传）
    # default=None 表示如果用户没传这个字段，值就是 None
    # max_length=500 — 描述最多 500 个字符
    # ========================================================================
    description: str | None = Field(
        default=None,
        max_length=500,
        description="任务描述，最多500个字符（可选）",
    )

    # ========================================================================
    # priority: 任务优先级（可选，默认1）
    # ========================================================================
    # default=1  — 用户不传的话默认是 1（普通优先级）
    # ge=1, le=5 — 值必须在 1 到 5 之间（greater/less or equal）
    # ========================================================================
    priority: int = Field(
        default=1,
        ge=1,
        le=5,
        description="任务优先级，1-5之间（数值越大越优先）",
    )


# ============================================================================
# Task — 返回给客户端的"响应体"模型
# ============================================================================
# 这个类继承了 TaskCreate（冒号右边是小括号，表示继承），
# 所以它拥有 TaskCreate 的所有字段（title, description, priority），
# 并且额外多了一个 task_id 字段。
#
# 为什么要把请求和响应拆成两个模型？
#   - 请求模型（TaskCreate）：用户不需要传 task_id，传了也不安全
#   - 响应模型（Task）    ：返回时带上 task_id，方便用户后续操作
#   这就是"关注点分离"的思想雏形
# ============================================================================

class Task(TaskCreate):
    """
    任务完整数据模型（包含服务端自动生成的 ID）。

    当服务端创建或查询任务时，返回给客户端的就是这个格式。
    """

    # ========================================================================
    # task_id: 任务唯一标识（自动生成，只读）
    # ========================================================================
    # 注意这里没有 default，因为每个任务都必须有一个 ID
    # 这个 ID 由服务端在创建任务时自动分配（自增计数器）
    # ========================================================================
    task_id: int
