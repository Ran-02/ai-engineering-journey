"""
================================================================================
 db/models.py — 数据库模型定义
================================================================================

 这个文件定义了"数据库里的表长什么样"。
 每个类对应一张表，每个类属性对应一列。

 ────────────────────────────────────────────────────────────────────────────
 数据库模型 vs 接口模型（双层模型分离）

 本文件只定义"数据库模型"（DB Model），它对应的是数据库里的表。
 接口里用什么格式返回给前端，那是 schemas/ 的事。

   数据库模型（本文件）         接口模型（schemas/task.py）
   ┌──────────────────┐       ┌──────────────────┐
   │ task_id           │       │ task_id           │
   │ title             │ ───→  │ title             │
   │ description       │       │ description       │
   │ priority          │       │ priority          │
   └──────────────────┘       └──────────────────┘
   面向数据库                   面向客户端
   可以包含内部字段               只暴露需要的字段
   直接映射表结构                可以定制输出格式

 这样做的好处：
   1. 改了数据库表结构，不一定需要改接口（加个中间字段不影响前端）
   2. 敏感字段（如 is_deleted）不会意外暴露给客户端
   3. 职责清晰：一个只管存，一个只管传
 ================================================================================
"""

# ============================================================================
# SQLAlchemy 2.0 的类型导入
# ============================================================================
# 在 SQLAlchemy 2.0 中，用 Mapped 和 mapped_column 来定义字段。
# 这种写法比旧版更简洁，而且有更好的类型提示。
#
#   Mapped[int]        → 声明这个字段的类型是整数（对应数据库 INTEGER）
#   Mapped[str]        → 声明这个字段的类型是字符串（对应数据库 VARCHAR）
#   mapped_column(...)   → 配置字段的约束条件（主键、默认值等）
# ============================================================================
from sqlalchemy import Integer, String
from sqlalchemy.orm import Mapped, mapped_column

# 从 database.py 导入 Base（所有模型的父类）
from app.db.database import Base


# ============================================================================
# TaskDB — 任务数据库模型
# ============================================================================
# 这个类对应数据库中的 tasks 表。
# 继承 Base 后，SQLAlchemy 会自动识别它并创建对应的表。

# 命名说明：
#   类名叫 TaskDB 而不是 Task，是为了和 schemas/task.py 中的 Task 区分开。
#   TaskDB  → 数据库模型（存数据用的）
#   Task    → 接口模型（返回给前端用的）
#   虽然不是强制要求，但这种命名方式让代码更清晰。
# ============================================================================

class TaskDB(Base):
    """
    任务数据库模型。

    每个实例对应 tasks 表中的一行记录。
    """

    # __tablename__ 指定数据库中的表名（必填！）
    # 这里叫 "tasks"（复数），符合数据库表命名惯例
    __tablename__ = "tasks"

    # ========================================================================
    # task_id: 主键（自增整数）
    # ========================================================================
    # primary_key=True  → 主键，唯一标识每条记录
    # autoincrement=True → 自动递增（每插入一条，值自动 +1）
    #                      SQLite 默认自增，这个参数可省略
    # index=True        → 为这个字段创建索引（加快按 ID 查询的速度）
    #
    # 关于字段命名：
    #   为什么用 task_id 而不是 id？
    #   为了和接口保持一致（接口返回的字段名叫 task_id），
    #   避免在 model_validate 时因字段名不一致而出错。
    # ========================================================================
    task_id: Mapped[int] = mapped_column(
        Integer,
        primary_key=True,
        autoincrement=True,
        index=True,
        comment="任务唯一ID（自增主键）",
    )

    # ========================================================================
    # title: 任务标题（必填，最长 50 字符）
    # ========================================================================
    # String(50) → 数据库中的 VARCHAR(50)，最多 50 个字符
    # nullable=False → 不可为空（默认就是 False，可省略）
    # 注意：这里的 nullable=False 和 Pydantic 的 min_length=1 是两层校验：
    #   - nullable=False 是数据库层的约束（数据库不让存空值）
    #   - min_length=1 是接口层的约束（接口不让传空字符串）
    #   各管各的，互不冲突
    # ========================================================================
    title: Mapped[str] = mapped_column(
        String(50),
        nullable=False,
        comment="任务标题",
    )

    # ========================================================================
    # description: 任务描述（可选，最长 500 字符）
    # ========================================================================
    # Mapped[str | None] → 这个字段可以是字符串或 None
    # nullable=True      → 数据库允许这个字段为空
    # default=None       → 如果不传值，默认是 None
    # ========================================================================
    description: Mapped[str | None] = mapped_column(
        String(500),
        nullable=True,
        default=None,
        comment="任务描述",
    )

    # ========================================================================
    # priority: 任务优先级（默认 1，范围 1-5）
    # ========================================================================
    # Integer       → 数据库中的整数类型
    # default=1     → 如果插入时不传值，默认为 1
    # 注意：SQLAlchemy 的 default 是"数据库层面的默认值"，
    # 而 Pydantic 的 Field(default=1) 是"Python 层面的默认值"。
    # 这里两者都设默认值，双重保险。
    # ========================================================================
    priority: Mapped[int] = mapped_column(
        Integer,
        default=1,
        comment="任务优先级，1-5",
    )
