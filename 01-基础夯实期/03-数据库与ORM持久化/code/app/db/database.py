"""
================================================================================
 db/database.py — 数据库三大核心组件配置
================================================================================

 这个文件是整个数据库操作的"基础设施层"，负责三件事：

   1. Engine（引擎）     → 连接数据库，管理连接池
   2. SessionLocal（会话工厂）→ 生产数据库会话的"工厂函数"
   3. Base（基类）       → 所有数据库模型的"祖宗"

 另外还提供了两个工具函数：
   - get_db()   → 给 FastAPI 用的依赖注入（每个请求一个会话，用完自动关）
   - init_db()  → 创建所有数据库表（项目启动时调用一次）

 ────────────────────────────────────────────────────────────────────────────
 三大组件的比喻理解

   Engine       = 快递总站   → 负责修路（连数据库）和管理车队（连接池）
   Session      = 快递员     → 每次取货送货（增删改查）都找他
   Base         = 快递单模板  → 所有包裹（数据表）都按这个模板填写
   SessionLocal = 快递公司    → 需要快递员时，打电话叫公司派一个过来

 ────────────────────────────────────────────────────────────────────────────
 为什么 Session 要用 "工厂模式" (SessionLocal)？

 每个请求都需要一个独立的数据库会话（就像每个顾客需要一个独立的服务员）。
 如果所有请求共用一个会话，会导致：
   - 事务互相干扰（A 请求还没提交，B 请求看到了脏数据）
   - 连接无法释放（一个请求卡住，所有请求都等着）

 SessionLocal 就是"会话工厂"——每次调用都创建一个全新的会话。
 ================================================================================
"""

from sqlalchemy import create_engine
from sqlalchemy.orm import DeclarativeBase, Session, sessionmaker

from app.utils.logger import logger

# ============================================================================
# 数据库配置
# ============================================================================

# SQLite 数据库文件路径
# "sqlite:///./task.db" 的意思是：
#   sqlite://    → 使用 SQLite 数据库
#   /./task.db   → 在当前工作目录下创建 task.db 文件
# 这个文件会自动生成，不需要手动创建
DATABASE_URL = "sqlite:///./task.db"


# ============================================================================
# 1. Engine（引擎）— 负责连接数据库
# ============================================================================
# create_engine 做的事：
#   1. 根据 URL 连接到对应的数据库（这里是 SQLite）
#   2. 自动维护一个"连接池"，省去反复建立连接的开销
#   3. 把 ORM 操作翻译成 SQL 语句发给数据库执行
#
# 参数说明：
#   DATABASE_URL  → 连接地址
#   echo=True     → 在控制台打印实际执行的 SQL 语句（开发调试利器）
#   connect_args  → SQLite 特有的参数（后面详细解释）
# ============================================================================

# ─── 关于 connect_args={"check_same_thread": False} ───
# 这是 SQLite 特有的参数。默认情况下，SQLite 只允许"创建连接的线程"使用该连接。
# 但 FastAPI 的请求可能由不同线程处理。加上这个参数后，SQLite 允许跨线程访问。
# 其他数据库（MySQL、PostgreSQL）不需要这个参数。
# ────────────────────────────────────────────────────────

engine = create_engine(
    DATABASE_URL,
    echo=True,                                    # 打印 SQL 语句
    connect_args={"check_same_thread": False},     # SQLite 跨线程访问
)


# ============================================================================
# 2. SessionLocal（会话工厂）— 负责创建数据库会话
# ============================================================================
# sessionmaker 是一个"工厂函数"：
#   调用 SessionLocal()  →  返回一个全新的 Session 实例
#   再调用 SessionLocal() →  又返回一个全新的 Session 实例
#   每次调用都是独立的，互不干扰
#
# 参数说明：
#   autocommit=False  → 禁止自动提交（必须手动 commit，更安全）
#   autoflush=False   → 禁止自动刷新（手动控制查询时机）
#   bind=engine       → 绑定到上面创建的引擎
# ============================================================================

SessionLocal = sessionmaker(autocommit=False, autoflush=False, bind=engine)


# ============================================================================
# 3. Base（基类）— 所有数据库模型的父类
# ============================================================================
# 所有数据库模型（表）都要继承这个 Base。
# Base 内部维护了一个所有模型的注册表，
# 调用 Base.metadata.create_all(engine) 时会遍历所有子类，自动建表。
# ============================================================================

class Base(DeclarativeBase):
    pass


# ============================================================================
# 4. get_db（依赖注入）— 每个请求一个会话
# ============================================================================
# 这是 FastAPI 的依赖函数，使用了 yield 关键字。
# yield 的执行流程（重要！）：
#
#   请求来了
#     │
#     ▼
#   db = SessionLocal()      ← 创建新会话（第 1 步）
#     │
#     ▼
#   yield db                 ← 暂停，把 db 注入给接口用（第 2 步）
#     │                         ─── 接口在这里执行 CRUD ───
#     ▼
#   finally: db.close()      ← 接口执行完了，关闭会话（第 3 步）
#
# 关键点：
#   - yield 之前的代码 → "请求前"执行
#   - yield 把值传出去 → 接口拿到 db 进行操作
#   - finally 中的代码 → "请求后"一定执行（无论接口是否报错）
#
# 这种模式保证：每个请求创建且只创建一个会话，请求结束时一定关闭。
# 不会出现"开了会话忘了关"导致的连接泄露。
# ============================================================================

def get_db():
    """
    数据库会话依赖注入函数。

    在每个请求中这样使用：
        def read_tasks(db: Session = Depends(get_db)):
            ...

    FastAPI 会自动：
      1. 调用 get_db() → 创建 Session
      2. 把 Session 注入给接口的 db 参数
      3. 接口执行完毕后，继续执行 get_db() 中 yield 后面的代码 → 关闭 Session
    """
    db = SessionLocal()  # 第 1 步：创建新会话
    try:
        yield db          # 第 2 步：交给接口函数使用
    finally:
        db.close()        # 第 3 步：请求结束，关闭会话
        # 注意：这里没有 commit，commit 应该在接口里显式调用
        # 也没有 rollback，因为异常时由全局异常处理器决定如何处理


# ============================================================================
# 5. init_db（初始化）— 启动时自动建表
# ============================================================================
# 这个函数遍历所有继承 Base 的模型类，在数据库中创建对应的表。
# 相当于执行了 CREATE TABLE IF NOT EXISTS ...
#
# 调用时机：项目启动时（在 main.py 的 lifespan 中调用），只需调用一次。
# ============================================================================

def init_db():
    """
    创建所有数据库表（如果表已存在则跳过）。

    原理：
      Base.metadata 中注册了所有继承 Base 的模型类的表结构信息。
      create_all(engine) 遍历这些信息，为每个表执行 CREATE TABLE SQL。

    注意：
      - 表已存在不会重复创建（不会覆盖已有数据）
      - 模型新增字段后，不会自动修改表结构（需要数据库迁移工具 Alembic）
    """
    logger.info("正在创建数据库表...")
    Base.metadata.create_all(bind=engine)
    logger.info("✅ 数据库表创建完成")
