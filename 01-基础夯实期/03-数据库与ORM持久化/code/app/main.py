"""
================================================================================
 main.py — 应用主入口（总装车间）
================================================================================

 这个文件只做四件事，按顺序：
   1. 创建 FastAPI 应用实例
   2. 注册异常处理器（优先）
   3. 注册中间件（耗时统计 + 访问日志）
   4. 挂载路由

 新增（相比第 2 周）：
   - lifespan 生命周期管理 → 启动时自动创建数据库表
   - init_db() 调用       → 无需手动执行 SQL 建表

 ────────────────────────────────────────────────────────────────────────────
 注册顺序为什么重要？

   1. 异常处理器（最先）
      → 确保任何阶段出错都能被捕获

   2. 中间件
      → 每个请求都会经过，洋葱模型的外皮

   3. 路由（最后）
      → 实际业务逻辑，err 时被前面的异常处理器兜底

   4. 生命周期（包裹一切）
      → 启动时建表，关闭时清理资源
================================================================================
"""

import time
from contextlib import asynccontextmanager

from fastapi import FastAPI, Request

from app.routers.task import router as task_router
from app.utils.exceptions import register_exception_handlers
from app.utils.logger import logger
from app.db.database import init_db


# ============================================================================
# 生命周期管理 — 启动时自动建表
# ============================================================================
# asynccontextmanager 是 Python 的异步上下文管理器装饰器。
# 使用 lifespan 模式替代旧的 @app.on_event("startup") / ("shutdown")，
# 这是 FastAPI 推荐的新写法。
#
# 执行流程：
#   yield 之前 → 应用启动时执行（建表、初始化连接池等）
#   yield      → 应用运行中
#   yield 之后 → 应用关闭时执行（清理资源、关闭连接池等）
# ============================================================================

@asynccontextmanager
async def lifespan(app: FastAPI):
    """
    应用生命周期管理。

    启动时：
      1. 记录启动日志
      2. 调用 init_db() 自动创建数据库表
      3. 提示接口文档地址

    关闭时：
      （本项目无需要清理的资源，留空即可）
    """
    logger.info("🚀 应用启动中...")
    # 启动时自动创建数据库表（如果表已存在则跳过，不会覆盖数据）
    init_db()
    logger.info("✅ 数据库就绪")
    logger.info("📖 接口文档: http://127.0.0.1:8000/docs")
    yield
    logger.info("应用已关闭")


# ============================================================================
# 第一步：创建 FastAPI 应用实例
# ============================================================================
app = FastAPI(
    title="任务管理系统",
    description="第3周数据库持久化版 — FastAPI + SQLAlchemy + SQLite",
    version="3.0.0",
    lifespan=lifespan,          # 注册生命周期处理器
)


# ============================================================================
# 第二步：注册异常处理器
# ============================================================================
register_exception_handlers(app)
logger.info("✅ 异常处理器注册完成")


# ============================================================================
# 第三步：注册中间件
# ============================================================================

@app.middleware("http")
async def process_time_middleware(request: Request, call_next):
    """
    请求耗时统计中间件。

    洋葱模型流程：
      请求进入 → 记时间 → call_next(请求) → 路由处理 → 回来算耗时 → 注入响应头
    """
    start_time = time.perf_counter()
    logger.info(f"→ {request.method} {request.url.path}")

    response = await call_next(request)

    elapsed = time.perf_counter() - start_time
    response.headers["X-Process-Time"] = f"{elapsed:.4f}"

    logger.info(
        f"← {request.method} {request.url.path} "
        f"[{response.status_code}] {elapsed:.4f}s"
    )
    return response


# ============================================================================
# 第四步：注册路由
# ============================================================================
app.include_router(task_router)
logger.info("✅ 路由注册完成")


# ============================================================================
# 根路径欢迎接口
# ============================================================================
@app.get("/", summary="根路径欢迎")
def root():
    return {"message": "欢迎使用任务管理系统"}


# ============================================================================
# 启动入口
# ============================================================================
if __name__ == "__main__":
    import uvicorn

    uvicorn.run("app.main:app", reload=True)
